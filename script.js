function createList(datas) {
    var list = document.getElementById("words");
    
    for (var i = 0; i < datas.length; i++) {
        var data = datas[i];
        var li = document.createElement("li");
        li.className = "word";

        var title = document.createElement("p");
        if (data.sprache === "de") {
            title.className = "titel";
        }
        else {
            title.className = "baslik";
        }
        title.textContent = data.titel;

        var subtitle = document.createElement("span");
        subtitle.className = "untertitel";
        subtitle.textContent = data.untertitel;

        var details = document.createElement("div");
        details.className = "details";

        details.appendChild(title);
        details.appendChild(subtitle);

        var content = document.createElement("div");
        content.className = "inhalt";

        var meaningsTitle = document.createElement("p");
        meaningsTitle.textContent = "Bedeutungen";

        var meanings = document.createElement("span");
        meanings.textContent = data.bedeutungen;

        content.appendChild(meaningsTitle);
        content.appendChild(meanings);

        li.appendChild(details);
        li.appendChild(content);

        if (data.beispiele) {
            var examples = document.createElement("div");
            examples.className = "beispiele";

            var examplesTitle = document.createElement("p");
            examplesTitle.textContent = "Beispiele";
            
            var examplesContent = document.createElement("span");
            examplesContent.innerHTML = data.beispiele;

            examples.appendChild(examplesTitle);
            examples.appendChild(examplesContent);

            li.appendChild(examples);
        }

        list.appendChild(li);


        var input = document.getElementById("input").value.toLowerCase();
    var list = document.getElementById("words");
    var words = list.getElementsByClassName("word");

    for (var i = 0; i < words.length; i++) {
        var word = words[i].getElementsByClassName("details")[0].getElementsByTagName("p")[0].innerText.toLowerCase();
        if (input.length > 1 && word.includes(input)) {
            words[i].style.display = "block";
        } else {
            words[i].style.display = "none";
        }
    }
    }
}

function search() {
    var input = document.getElementById("input").value.toLowerCase();
    var list = document.getElementById("words");
    var words = list.getElementsByClassName("word");

    for (var i = 0; i < words.length; i++) {
        var word = words[i].getElementsByClassName("details")[0].getElementsByTagName("p")[0].innerText.toLowerCase();
        if (input === "@"){
            words[i].style.display = "block";
        }
        else if (input === "o" && word.startsWith(input)) {
            words[i].style.display = "block";
        }
        else if (input.length > 1 && word.includes(input)) {
            words[i].style.display = "block";
        } else {
            words[i].style.display = "none";
        }
    }
}
